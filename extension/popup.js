/**
 * Override the daily limit and continue reporting
 */
async function overrideDailyLimit(platform) {
  if (platform === 'instagram') {
    igLimitOverridden = true;
  } else {
    twLimitOverridden = true;
  }

  // Store the override in storage so background.js knows
  const prefix = platform === 'instagram' ? 'ig' : 'tw';
  const today = getTodayKey();
  await chrome.storage.local.set({
    [`${prefix}_limit_override`]: today,
    // Clear the "paused due to limit" state when user explicitly overrides and continues.
    [`${prefix}_limitPaused`]: false,
  });

  // Hide the banner
  $(`${prefix}-rate-limit-banner`).classList.remove('active');

  // Resume the bot
  await loadDailyCounts();
  await resume(platform);
}

/**
 * Stop the bot due to daily limit
 */
async function stopForLimit(platform) {
  // "Stop for Today" should keep progress (so Resume remains possible),
  // but should pause execution by ensuring isRunning is false.
  const prefix = platform === 'instagram' ? 'ig' : 'tw';
  await chrome.storage.local.set({ [`${prefix}_isRunning`]: false });

  // Hide the banner
  $(`${prefix}-rate-limit-banner`).classList.remove('active');
}

