let cooldownInterval = null;

/**
 * Format milliseconds as MM:SS
 */
function formatCooldownTime(ms) {
  if (ms <= 0) return '00:00';
  const totalSeconds = Math.ceil(ms / 1000);
  const minutes = Math.floor(totalSeconds / 60);
  const seconds = totalSeconds % 60;
  return `${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
}

/**
 * Update the cooldown timer display for both platforms
 */
async function updateCooldownTimers() {
  const s = await chrome.storage.local.get([
    'ig_cooldownEndsAt', 'ig_isRunning',
    'tw_cooldownEndsAt', 'tw_isRunning'
  ]);

  const now = Date.now();
  let anyCooldownActive = false;

  // Instagram cooldown
  const igCooldownEndsAt = s.ig_cooldownEndsAt;
  const igBanner = $('ig-cooldown-banner');
  const igTimer = $('ig-cooldown-timer');

  if (igCooldownEndsAt && s.ig_isRunning && igCooldownEndsAt > now) {
    const remaining = igCooldownEndsAt - now;
    igTimer.textContent = formatCooldownTime(remaining);
    igBanner.classList.add('active');
    anyCooldownActive = true;
  } else {
    igBanner.classList.remove('active');
  }

  // Twitter cooldown
  const twCooldownEndsAt = s.tw_cooldownEndsAt;
  const twBanner = $('tw-cooldown-banner');
  const twTimer = $('tw-cooldown-timer');

  if (twCooldownEndsAt && s.tw_isRunning && twCooldownEndsAt > now) {
    const remaining = twCooldownEndsAt - now;
    twTimer.textContent = formatCooldownTime(remaining);
    twBanner.classList.add('active');
    anyCooldownActive = true;
  } else {
    twBanner.classList.remove('active');
  }

  // Start or stop the interval based on whether any cooldown is active
  if (anyCooldownActive) {
    // Only start a new interval if one isn't already running
    if (!cooldownInterval) {
      cooldownInterval = setInterval(updateCooldownTimers, 1000);
    }
  } else {
    // No cooldown active - clear interval and trigger a UI refresh
    if (cooldownInterval) {
      clearInterval(cooldownInterval);
      cooldownInterval = null;
      // Trigger a check to update the bot status from "Cooling down..." back to "Running..."
      requestCheck();
    }
  }
}

