  const text = $(`${prefix}-daily-text`);
  const banner = $(`${prefix}-rate-limit-banner`);

  if (!fill || !text || !banner) return;

  const percentage = Math.min((count / DAILY_LIMIT) * 100, 100);
  fill.style.width = `${percentage}%`;
